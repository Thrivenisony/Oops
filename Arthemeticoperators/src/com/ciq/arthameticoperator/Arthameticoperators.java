package com.ciq.arthameticoperator;

public class Arthameticoperators {
	public static void main(String[] args) {
		int a=10;
		int b=20;
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a%b);
		System.out.println(a/b);
		byte numone=10;
		byte numtwo=20;
		byte total=(byte)(numone+numtwo);
		System.out.println(total);
	}

}

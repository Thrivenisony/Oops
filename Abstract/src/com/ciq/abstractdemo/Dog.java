package com.ciq.abstractdemo;

public class Dog extends Animal {

	@Override
	public void eat() {
		System.out.println("dog eats");
		
	}

	@Override
	public void drink() {
	System.out.println("dog drinks");
		
	}

	@Override
	public void sleep() {
		System.out.println("dog sleep");
		
	}

}
